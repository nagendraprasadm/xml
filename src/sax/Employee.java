package sax;

public class Employee {
	
	private String _fName;
	private String _lName;
	private String _nName;
	private long _sal;
	public String get_fName() {
		return _fName;
	}
	public void set_fName(String _fName) {
		this._fName = _fName;
	}
	public String get_lName() {
		return _lName;
	}
	public void set_lName(String _lName) {
		this._lName = _lName;
	}
	public String get_nName() {
		return _nName;
	}
	public void set_nName(String _nName) {
		this._nName = _nName;
	}
	public long get_sal() {
		return _sal;
	}
	public void set_sal(long _sal) {
		this._sal = _sal;
	}

}
